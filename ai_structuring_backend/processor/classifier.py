    def _semantic_alias_lookup(self, raw_tag: str, meta: dict | None = None) -> str | None:
        """Corpus-derived alias remap for publisher-specific style names.

        Checks raw_tag against the semantic_alias_map built from
        style_alias_candidates.json (add_alias entries with confidence >=
        _MIN_ALIAS_CONFIDENCE).  Re-validates the suggested canonical through
        normalize_tag() before returning so the result is always a member of
        allowed_styles.  Returns None when no confident mapping exists.
        """
        alias_map = getattr(self, "_semantic_hints", {}).get("semantic_alias_map", {})
        entry = alias_map.get(raw_tag)
        if not entry:
            return None
        candidate = entry["canonical"]
        validated = normalize_tag(candidate, meta=meta)
        logger.debug(
            "Semantic alias: '%s' -> '%s' (conf=%.2f, validated='%s')",
            raw_tag, candidate, entry["confidence"], validated,
        )
        return validated

    # Module-level dicts are defined here for clarity; they reference no instance state.
    _LIST_KIND_TO_BASE: dict[str, str] = {
        "unordered":  "BL",
        "ordered":    "NL",
        "numbered":   "NL",
        "unnumbered": "UL",
    }
    _LIST_POS_TO_SUFFIX: dict[str, str] = {
        "first": "FIRST",
        "mid":   "MID",
        "last":  "LAST",
        "only":  "FIRST",   # single-item list treated as first
    }

    def _transition_positional_hint(
        self, mapped_tag: str, meta: dict | None
    ) -> str | None:
        """Use list-position metadata + positional_transitions to suggest a canonical form.

        Applies only when metadata contains list_kind (set by the block extractor).
        The positional_transitions corpus data confirms which FIRST/MID/LAST forms
        exist for each list base.  Returns None when metadata is absent or the
        candidate cannot be validated.
        """
        if not meta:
            return None
        list_kind = meta.get("list_kind")
        if not list_kind:
            return None
        base = self._LIST_KIND_TO_BASE.get(list_kind.lower())
        if not base:
            return None

        # Resolve positional suffix from metadata (default MID — most common state)
        list_pos = meta.get("list_position", "mid")
        suffix = self._LIST_POS_TO_SUFFIX.get(
            list_pos.lower() if list_pos else "mid", "MID"
        )
        candidate = f"{base}-{suffix}"

        # Confirm the candidate form appears in the corpus transition matrix
        positional = getattr(self, "_semantic_hints", {}).get("positional_transitions", {})
        if positional and candidate not in positional:
            # Try the generic MID form for this family
            fallback = f"{base}-MID"
            if fallback in positional:
                candidate = fallback
            else:
                return None  # family not represented — don't guess

        validated = normalize_tag(candidate)
        if validated in VALID_TAGS:
            logger.debug(
                "Transition positional hint: list_kind='%s' pos='%s' -> '%s'",
                list_kind, list_pos, validated,
            )
            return validated
        return None

    def _force_invalid_to_txt(
        self,
        results: list[dict],
        meta_by_id: dict | None = None,
        text_by_id: dict | None = None
    ) -> list[dict]:
        """Normalize and validate tags with a tiered semantic fallback chain.

        For each classification result the following steps are applied in order:

        1. _map_tag_alias()   — zone-aware alias expansion (existing)
        2. normalize_tag()    — enforce membership in allowed_styles.json (existing)

        If step 2 returns a generic placeholder (TXT/TXT-FLUSH) and the mapped
        tag is itself not a known valid style, the chain continues:

        3. Semantic alias lookup — corpus-derived publisher-style remaps from
           style_alias_candidates.json (no corpus file dependency at runtime).
        4. Transition positional hint — list_kind + list_position metadata drives
           the canonical FIRST/MID/LAST form, confirmed by corpus transitions.
        5. Grounded retriever — TF-IDF similarity search over ground_truth.jsonl,
           only when ENABLE_GROUNDED_RETRIEVER=true and MODE=invalid_tag_fallback.

        Every suggested tag is re-validated through normalize_tag() before use.
        Zone constraints remain enforced downstream by style_enforcement.py.
        """
        for r in results:
            rid = r.get("id")
            meta = meta_by_id.get(rid) if meta_by_id else None
            text = text_by_id.get(rid, "") if text_by_id else ""
            raw_tag = r.get("tag", "")

            # Step 1: zone-aware alias mapping
            mapped_tag = self._map_tag_alias(raw_tag, meta=meta, text=text)

            # Step 2: enforce membership in allowed_styles.json
            normalized_tag = normalize_tag(mapped_tag, meta=meta)

            # === SEMANTIC FALLBACK CHAIN ===
            # Entered whenever the mapped_tag is not a clean canonical VALID_TAG.
            # This covers two cases:
            #   (a) normalize_tag() fell back to TXT/TXT-FLUSH (tag wholly unrecognized)
            #   (b) normalize_tag() returned a publisher style that IS in allowed_styles.json
            #       but is NOT a clean canonical (e.g. "Head2", "Normal") — those need
            #       remapping to their canonical equivalent via semantic alias lookup.
            if mapped_tag not in VALID_TAGS:

                # Step 3: Semantic alias candidates (corpus-derived, no runtime corpus dep).
                # Search order: raw_tag → mapped_tag → normalized_tag (publisher pass-through).
                alias_tag = self._semantic_alias_lookup(raw_tag, meta=meta)
                if alias_tag is None and mapped_tag != raw_tag:
                    alias_tag = self._semantic_alias_lookup(mapped_tag, meta=meta)
                if alias_tag is None and normalized_tag not in VALID_TAGS and normalized_tag != mapped_tag:
                    alias_tag = self._semantic_alias_lookup(normalized_tag, meta=meta)
                if alias_tag is not None:
                    normalized_tag = alias_tag
                    logger.debug(
                        "Fallback resolved via semantic alias: '%s' -> '%s'",
                        raw_tag, normalized_tag,
                    )

                # Steps 4 & 5 only needed when the tag is still completely unresolved
                # (normalize_tag returned TXT/TXT-FLUSH = it could not find any match).
                # Step 4: Transition-priors positional hint (list metadata + corpus data)
                if normalized_tag in {"TXT", "TXT-FLUSH"}:
                    pos_tag = self._transition_positional_hint(mapped_tag, meta)
                    if pos_tag:
                        normalized_tag = pos_tag
                        logger.debug(
                            "Fallback resolved via positional hint: '%s' -> '%s'",
                            raw_tag, normalized_tag,
                        )

                # Step 5: Grounded retriever — optional, requires explicit env opt-in
                if (
                    normalized_tag in {"TXT", "TXT-FLUSH"}
                    and self.retriever
                    and is_invalid_tag_fallback_enabled()
                    and text
                ):
                    try:
                        zone = (meta or {}).get("context_zone", "BODY")
                        similar = self.retriever.retrieve_examples(
                            text=text, k=1, zone=zone
                        )
                        if similar and similar[0].get("similarity_score", 0) > 0.7:
                            grounded_tag = similar[0].get("canonical_gold_tag", "TXT")
                            validated_grounded = normalize_tag(grounded_tag, meta=meta)
                            if validated_grounded != "TXT" or grounded_tag == "TXT":
                                normalized_tag = validated_grounded
                                logger.debug(
                                    "Fallback resolved via grounded retriever: '%s' -> '%s'"
                                    " (similarity=%.3f)",
                                    raw_tag, normalized_tag,
                                    similar[0].get("similarity_score", 0),
                                )
                    except Exception as exc:
                        logger.warning("Grounded fallback failed: %s", exc)

            # Persist the resolved tag and cap confidence for normalised results
            if normalized_tag != raw_tag:
                r["tag"] = normalized_tag
                r["confidence"] = min(int(r.get("confidence", 50)), 60)
                r["reasoning"] = f"Normalized '{raw_tag}' -> '{normalized_tag}'"
            else:
                r["tag"] = normalized_tag

        return results
    
    def _classify_chunk(
        self,
        paragraphs: list[dict],
        document_name: str,
        document_type: str,
        chunk_info: str = ""
    ) -> list[dict]:
        """
        Classify a chunk of paragraphs.
        Retries are handled by GeminiClient internally.
        """
        leaked_skip_ids = [p.get("id") for p in paragraphs if p.get("skip_llm") is True]
        if leaked_skip_ids:
            logger.warning(
                "skip-llm: blocks %s reached LLM payload build path; aborting chunk",
                leaked_skip_ids,
            )
            raise AssertionError(
                f"skip_llm blocks reached LLM payload build path: {leaked_skip_ids}"
            )

        user_prompt = self.build_user_prompt(paragraphs, document_name, document_type, chunk_info)

        logger.info(f"Sending {len(paragraphs)} paragraphs to Gemini API")

        # Make API call (retries handled internally by GeminiClient)
        response = self._generate_content(user_prompt)
        logger.info("Received response from Gemini API")

        # Log token usage (tracked by GeminiClient)
        last_usage = self.model.get_last_usage()
        total_usage = self.model.get_token_usage()

        if last_usage:
            # Format token values, handling None gracefully
            input_tok = last_usage.get('input_tokens')
            output_tok = last_usage.get('output_tokens')
            total_tok = last_usage.get('total_tokens')

            logger.info(
                f"Token Usage - Input: {input_tok:,}, "
                f"Output: {output_tok:,}, "
                f"Total: {total_tok:,}"
            )

            # Format cumulative values, handling None gracefully
            cum_input = total_usage.get('total_input_tokens')
            cum_output = total_usage.get('total_output_tokens')
            cum_total = total_usage.get('total_tokens')

            if cum_input is not None and cum_output is not None and cum_total is not None:
                logger.info(
                    f"Cumulative Tokens - Input: {cum_input:,}, "
                    f"Output: {cum_output:,}, "
                    f"Total: {cum_total:,}"
                )
            else:
                logger.info(
                    f"Cumulative Tokens - Input: {cum_input or 0}, "
                    f"Output: {cum_output or 0}, "
                    f"Total: {cum_total or 0}"
                )
        else:
            logger.warning("Token usage metadata not available in response")

        # Parse JSON response with robust handling
        results = self._parse_json_response(response.text, len(paragraphs))
        meta_by_id = {p.get("id"): p.get("metadata", {}) for p in paragraphs}
        text_by_id = {p.get("id"): p.get("text", "") for p in paragraphs}
        results = self._apply_alias_mappings(results, meta_by_id=meta_by_id, text_by_id=text_by_id)

        # Self-heal if invalid tags detected
        invalid = self._find_invalid_tags(results, meta_by_id, text_by_id)
        if invalid:
            logger.warning(f"Invalid tags detected: {sorted(invalid)}. Retrying once with correction.")
            correction_prompt = (
                user_prompt
                + "\n\nINVALID TAGS FOUND: "
                + ", ".join(sorted(invalid))
                + "\nReturn corrected JSON using ONLY allowed tags."
            )
            response = self._generate_content(correction_prompt)
            results = self._parse_json_response(response.text, len(paragraphs))
            results = self._apply_alias_mappings(results, meta_by_id=meta_by_id, text_by_id=text_by_id)
            invalid = self._find_invalid_tags(results, meta_by_id, text_by_id)
            if invalid:
                logger.warning(f"Invalid tags persist after retry: {sorted(invalid)}. Using grounded fallback.")
                results = self._force_invalid_to_txt(results, meta_by_id, text_by_id)

        # Validate results for this chunk
        validated = self._validate_results(results, len(paragraphs), paragraphs[0]['id'])

        # Ensure no invalid tags remain after validation
        for r in validated:
            meta = meta_by_id.get(r.get("id")) if meta_by_id else None
            text = text_by_id.get(r.get("id"), "") if text_by_id else ""
            tag = self._map_tag_alias(r.get("tag", ""), meta=meta, text=text)
            if tag and tag not in VALID_TAGS:
                r["tag"] = "TXT"
                r["confidence"] = min(int(r.get("confidence", 50)), 50)
                r["reasoning"] = f"Invalid tag '{tag}' downgraded to TXT"
            else:
                r["tag"] = tag
            r["llm_generated"] = True  # Provenance: output produced by the LLM

        logger.info(f"Classified {len(validated)} paragraphs")
        return validated
    
    def _parse_json_response(self, response_text: str, expected_count: int) -> list[dict]:
        """
        Parse JSON response with multiple fallback strategies.
        """
        # Strategy 1: Direct parse
        try:
            results = json.loads(response_text)
            if isinstance(results, list):
                return results
        except json.JSONDecodeError:
            pass
        
        # Strategy 2: Extract JSON array from text
        try:
            json_match = re.search(r'\[[\s\S]*\]', response_text)
            if json_match:
                results = json.loads(json_match.group())
                if isinstance(results, list):
                    return results
        except json.JSONDecodeError:
            pass
        
        # Strategy 3: Fix truncated JSON
        try:
            fixed = self._fix_truncated_json(response_text)
            results = json.loads(fixed)
            if isinstance(results, list):
                logger.warning(f"Fixed truncated JSON, got {len(results)} items")
                return results
        except json.JSONDecodeError:
            pass
        
        # Strategy 4: Parse individual objects
        try:
            results = self._parse_individual_objects(response_text)
            if results:
                logger.warning(f"Parsed {len(results)} individual objects from malformed JSON")
                return results
        except Exception:
            pass
        
        # All strategies failed
        logger.error(f"Failed to parse JSON response: {response_text[:500]}...")
        raise ValueError(f"Failed to parse response as JSON after all strategies")
    
    def _fix_truncated_json(self, text: str) -> str:
        """
        Attempt to fix truncated JSON array.
        """
        # Remove any markdown code fences
        text = re.sub(r'```json\s*', '', text)
        text = re.sub(r'```\s*', '', text)
        text = text.strip()
        
        # Ensure it starts with [
        if not text.startswith('['):
            start = text.find('[')
            if start != -1:
                text = text[start:]
            else:
                return '[]'
        
        # If it doesn't end with ], try to fix
        if not text.rstrip().endswith(']'):
            # Find the last complete object
            last_complete = text.rfind('},')
            if last_complete != -1:
                text = text[:last_complete + 1] + ']'
            else:
                # Try to find last }
                last_brace = text.rfind('}')
                if last_brace != -1:
                    text = text[:last_brace + 1] + ']'
                else:
                    return '[]'
        
        return text
    
    def _parse_individual_objects(self, text: str) -> list[dict]:
        """
        Parse individual JSON objects from malformed response.
        """
        results = []
        
        # Find all JSON-like objects
        pattern = r'\{\s*"id"\s*:\s*(\d+)\s*,\s*"tag"\s*:\s*"([^"]+)"\s*,\s*"confidence"\s*:\s*(\d+)'
        
        for match in re.finditer(pattern, text):
            results.append({
                "id": int(match.group(1)),
                "tag": match.group(2),
                "confidence": int(match.group(3))
            })
        
        return results
    
