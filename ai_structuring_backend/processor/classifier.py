def _load_semantic_artifacts() -> dict:
    """Load tag_semantics_knowledge.json and tag_transition_priors.json.

    Returns an empty dict on missing files or parse errors so the caller
    degrades gracefully to the current generic prompt behaviour.
    """
    result: dict = {}
    try:
        if _SEMANTIC_KNOWLEDGE_PATH.exists():
            with open(_SEMANTIC_KNOWLEDGE_PATH, encoding="utf-8") as f:
                data = json.load(f)
            result["zone_tag_priors"] = data.get("zone_tag_priors", {})
    except Exception as exc:
        logger.debug("Semantic knowledge artifact not loaded: %s", exc)
    try:
        if _SEMANTIC_TRANSITIONS_PATH.exists():
            with open(_SEMANTIC_TRANSITIONS_PATH, encoding="utf-8") as f:
                data = json.load(f)
            # Pre-filter: retain only positional-list entries (FIRST/MID/LAST)
            # so per-prompt rendering is O(small-constant).
            global_trans = data.get("global_transitions", {})
            result["positional_transitions"] = {
                k: v for k, v in global_trans.items()
                if re.search(r"-(FIRST|MID|LAST)$", k)
            }
    except Exception as exc:
        logger.debug("Transition priors artifact not loaded: %s", exc)
    try:
        if _SEMANTIC_ALIAS_CANDIDATES_PATH.exists():
            with open(_SEMANTIC_ALIAS_CANDIDATES_PATH, encoding="utf-8") as f:
                data = json.load(f)
            semantic_alias_map: dict[str, dict] = {}
            for cand in data.get("candidates", []):
                if (
                    cand.get("recommendation") == "add_alias"
                    and cand.get("in_allowed_styles")
                    and cand.get("suggested_canonical")
                    and cand.get("confidence", 0) >= _MIN_ALIAS_CONFIDENCE
                ):
                    semantic_alias_map[cand["raw_style"]] = {
                        "canonical": cand["suggested_canonical"],
                        "confidence": cand["confidence"],
                    }
            result["semantic_alias_map"] = semantic_alias_map
    except Exception as exc:
        logger.debug("Style alias candidates not loaded: %s", exc)
    return result


